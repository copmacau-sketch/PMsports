function translateGsLeague(name: string): string {
  // Country prefix ("Brazil: Serie B") → 中文 + translated competition.
  const m = name.match(/^([^:]+):\s*(.+)$/);
  let country = "";
  let rest = name;
  if (m) {
    country = GS_COUNTRY_CN[m[1].trim()] ?? "";
    rest = m[2].trim();
  }
  let comp = rest;
  for (const [re, cn] of GS_KEYWORD_CN) comp = comp.replace(re, cn);
  // Collapse leftover separators / whitespace from removed sponsor tokens.
  comp = comp.replace(/\s*-\s*/g, " - ").replace(/\s{2,}/g, " ").trim();
  comp = comp.replace(/^-\s*|\s*-$/g, "").trim();
  const out = (country + (country && comp ? " " : "") + comp).trim();
  // Only return when we actually produced Chinese; otherwise let the caller
  // keep the English name rather than emit a half-translated string.
  return looksChinese(out) ? out : "";
}

export function translateLeague(slug: string | null | undefined, fallback?: string | null): string {
  if (slug && LEAGUE_CN[slug]) return LEAGUE_CN[slug];
  // Backend-provided Chinese (registry name_cn or machine-translated long
  // tail) before falling back to whatever display string the caller passed.
  if (slug && RUNTIME_LEAGUE_CN[slug]) return RUNTIME_LEAGUE_CN[slug];
  // The fallback (league_name) is frequently already Chinese from the league
  // registry; prefer it over a bare slug.
  if (fallback && looksChinese(fallback)) return fallback;
  // Goalserve English league names — exact map, then pattern translator.
  if (fallback) {
    if (GS_LEAGUE_CN[fallback]) return GS_LEAGUE_CN[fallback];
    const gs = translateGsLeague(fallback);
    if (gs) return gs;
  }
  return fallback ?? slug ?? "";
}

