// --- Runtime backend-provided translations ----------------------------------
// The curated maps above are authoritative. For the long tail of teams/leagues
// they don't cover, the backend (db_sports.name_cn_cache, filled by the
// translate_names cron) ships Simplified-Chinese names on each event as
// home_cn / away_cn / league_cn. We register those here at feed-ingest time so
// EVERY translateTeam / translateLeague call-site (list rows, detail markets,
// bet slip, order history) renders Chinese — not just the construction site.
// Curated dict always wins; runtime map only fills gaps; English is the last
// resort (transient, until the cron catches up).
const RUNTIME_TEAM_CN: Record<string, string> = {};
const RUNTIME_LEAGUE_CN: Record<string, string> = {};

/** Heuristic: a backend value is useful only if it actually contains CJK. */
function looksChinese(s: string): boolean {
  return /[\u4e00-\u9fff\u3400-\u4dbf]/.test(s);
}

export function registerTeamCn(en: string | null | undefined, cn: string | null | undefined): void {
  if (!en || !cn) return;
  const e = en.trim();
  const c = cn.trim();
  if (e === "" || c === "" || c === e) return;
  if (!looksChinese(c)) return;
  RUNTIME_TEAM_CN[e] = c;
}

export function registerLeagueCn(slug: string | null | undefined, cn: string | null | undefined): void {
  if (!slug || !cn) return;
  const s = slug.trim();
  const c = cn.trim();
  if (s === "" || c === "") return;
  if (!looksChinese(c)) return;
  RUNTIME_LEAGUE_CN[s] = c;
}

// Esports fixtures (Esoccer / GT Leagues) append the human player's gamer
// handle in parentheses, e.g. "Aston Villa (vladl3n)" / "Crystal Palace
// (Flewless_phoenix)".  The backend's name_cn_cache translates the club but
// leaves the Latin handle, so it leaks English into an otherwise-Chinese UI.
// Strip a TRAILING parenthetical only when its inner text is a player handle
// (contains a lowercase Latin letter and no CJK).  Age/gender/reserve markers
// like "(U21)", "(W)", "(B)", "(II)" are uppercase/Chinese and are preserved
// so men's/women's and first/reserve sides stay distinct.
function stripEsportsHandle(s: string): string {
  return s
    .replace(/\s*[（(]\s*([^（）()]*?)\s*[）)]\s*$/u, (full, inner: string) => {
      const hasCJK = /[\u4e00-\u9fff\u3400-\u4dbf]/.test(inner);
      const looksLikeHandle = /[a-z]/.test(inner);
      return !hasCJK && looksLikeHandle ? "" : full;
    })
    .trim();
}

export function translateTeam(name: string | null | undefined): string {
  if (!name) return "";
  const resolved = TEAM_CN[name] ?? RUNTIME_TEAM_CN[name] ?? name;
  return stripEsportsHandle(resolved);
}

