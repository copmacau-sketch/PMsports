/** Returns the league's full Chinese name, falling back to short name → raw. */
export function leagueFullName(
  slug: string | null | undefined,
  fallback?: string | null,
): string {
  const m = leagueMeta(slug);
  if (m) return m.fullName;
  return translateLeague(slug, fallback);
}

/** Returns the api-sports.io CDN logo URL, or null when no mapping exists. */
export function leagueLogoUrl(slug: string | null | undefined): string | null {
  const m = leagueMeta(slug);
  if (!m || m.apiSportsId == null) return null;
  return `https://media.api-sports.io/football/leagues/${m.apiSportsId}.png`;
}

